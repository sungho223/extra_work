package First_Homework;

// 정수형 변수 4개를 선언하고 값을 할당한다.
// 값이 할당된 정수형 변수 4개의 합을 구해 새로운 정수형 변수에 할당한다.
// 합이 할당된 변수의 값을 출력해본다.

public class Q7 {
    public static void main(String[] args) {
        int num1 = 1;
        int num2 = 2;
        int num3 = 3;
        int num4 = 4;

        int sum = num1 + num2 + num3 + num4;

        System.out.println("숫자의 합은: " + sum);

    }
}

// 숫자의 합은: 10